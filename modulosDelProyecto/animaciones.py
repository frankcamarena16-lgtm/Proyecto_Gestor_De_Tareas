def animacionDeCarga(mensaje:str,caracter:str):
    # Esta funcion 
    veces = random.randint(1,2)

    for v in range(veces):
        print(f"\r{mensaje}",end="")
        for c in range(4):
            print(f"{caracter}",end="",flush=True)
            time.sleep(0.3)
        print("\r                                           ",end="")